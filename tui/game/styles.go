const (
	EmptyCell     = "â–‘â–‘"
	FilledCell    = "â–ˆâ–ˆ"
	FoodCellApple = "ğŸ"

	SnakeHeadUp    = "â—“â—“"
	SnakeHeadDown  = "â—’â—’"
	SnakeHeadLeft  = "â—â—"
	SnakeHeadRight = "â—‘â—‘"

	PillarCell = "  "

	FoodColor = "#DC3A35"
)

func FoodCell() string {
	if utils.IsWindowsMachine() {
		return lipgloss.NewStyle().Foreground(lipgloss.Color(FoodColor)).Render(FilledCell)
	}

	return FoodCellApple
}

func SnakeHeadFromDirection(direction Direction) string {

	if utils.IsWindowsMachine() {
		return FilledCell
	}

	switch direction {
	case Up:
		return SnakeHeadUp
	case Down:
		return SnakeHeadDown
	case Left:
		return SnakeHeadLeft
	case Right:
		return SnakeHeadRight
	default:
		return SnakeHeadRight
	}
}

